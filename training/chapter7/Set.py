# 세트(Set)는 우리가 수학에서 배웠던 집합이다.
# 세트는 중복되지 않은 항목들이 모인 것
# 세트의 항목 간에는 순서가 없다.
# Set는 { 와 } 로 구분.

numbers = {2,1,3}
print(numbers)

print( len(numbers))

fruits = { "Apple" , "Banana", "Pineapple"}
print(fruits)

# 중복되게 있어도
mySet = {1.0, 2.0, 2.0, "Hello world", (1,2,3), 1.0, 1.0}

#출력을 하면 중복되지 않는것만 나옴.
print(mySet)